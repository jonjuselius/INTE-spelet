package Item;

public enum Type {
	/**
	 * Type is an enumeration that represents the category an item belongs to.
	 * Four types of item types are present as of now: weapon, armor, jewellery
	 * and food. Examples of items that are categorized as these item types are:
	 * sword: weapon; wand: weapon; egg: food; shield: armor; ring: jewellery.
	 */
	WEAPON, ARMOR, JEWELLERY, FOOD
}
